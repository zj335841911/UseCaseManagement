/**
 * 产品的分支和平台信息
 *
 * @export
 * @interface Branch
 */
export interface Branch {

    /**
     * 名称
     *
     * @returns {*}
     * @memberof Branch
     */
    name?: any;

    /**
     * 已删除
     *
     * @returns {*}
     * @memberof Branch
     */
    deleted?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof Branch
     */
    id?: any;

    /**
     * 排序
     *
     * @returns {*}
     * @memberof Branch
     */
    order?: any;

    /**
     * 所属产品
     *
     * @returns {*}
     * @memberof Branch
     */
    product?: any;
}
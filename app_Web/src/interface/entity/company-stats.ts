/**
 * 公司动态汇总
 *
 * @export
 * @interface CompanyStats
 */
export interface CompanyStats {

    /**
     * 登录次数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    logincnt?: any;

    /**
     * 新增需求数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    openedstorycnt?: any;

    /**
     * 日期
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    ztdate?: any;

    /**
     * 日志日期
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    date?: any;

    /**
     * 关闭需求数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    closedstorycnt?: any;

    /**
     * 新增Bug数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    openedbugcnt?: any;

    /**
     * 日志工时
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    loghours?: any;

    /**
     * 动态数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    dynamiccnt?: any;

    /**
     * 完成任务数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    finishedtaskcnt?: any;

    /**
     * 解决Bug数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    resolvedbugcnt?: any;

    /**
     * 备注
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    comment?: any;

    /**
     * 新增任务数
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    openedtaskcnt?: any;

    /**
     * 标识
     *
     * @returns {*}
     * @memberof CompanyStats
     */
    id?: any;
}
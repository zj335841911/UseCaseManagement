/**
 * 文档内容
 *
 * @export
 * @interface DocContent
 */
export interface DocContent {

    /**
     * 附件
     *
     * @returns {*}
     * @memberof DocContent
     */
    files?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof DocContent
     */
    id?: any;

    /**
     * 文档正文
     *
     * @returns {*}
     * @memberof DocContent
     */
    content?: any;

    /**
     * 文档类型
     *
     * @returns {*}
     * @memberof DocContent
     */
    type?: any;

    /**
     * 文档标题
     *
     * @returns {*}
     * @memberof DocContent
     */
    title?: any;

    /**
     * 版本号
     *
     * @returns {*}
     * @memberof DocContent
     */
    version?: any;

    /**
     * 文档摘要
     *
     * @returns {*}
     * @memberof DocContent
     */
    digest?: any;

    /**
     * 文档
     *
     * @returns {*}
     * @memberof DocContent
     */
    doc?: any;
}
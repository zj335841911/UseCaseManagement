/**
 * 群组
 *
 * @export
 * @interface Group
 */
export interface Group {

    /**
     * acl
     *
     * @returns {*}
     * @memberof Group
     */
    acl?: any;

    /**
     * 分组描述
     *
     * @returns {*}
     * @memberof Group
     */
    desc?: any;

    /**
     * ID
     *
     * @returns {*}
     * @memberof Group
     */
    id?: any;

    /**
     * 分组名称
     *
     * @returns {*}
     * @memberof Group
     */
    name?: any;

    /**
     * role
     *
     * @returns {*}
     * @memberof Group
     */
    role?: any;
}
/**
 * 系统插件
 *
 * @export
 * @interface IBIZProPlugin
 */
export interface IBIZProPlugin {

    /**
     * 版本
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    version?: any;

    /**
     * 类型
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    type?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    createman?: any;

    /**
     * 总下载量
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    downloadcount?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    createdate?: any;

    /**
     * 最新版本下载地址
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    downloadurl?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    updatedate?: any;

    /**
     * 标签
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    tag?: any;

    /**
     * 总评分
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    score?: any;

    /**
     * 系统插件名称
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    ibizpropluginname?: any;

    /**
     * 系统插件标识
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    ibizpropluginid?: any;

    /**
     * 关键字
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    keyword?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    updateman?: any;

    /**
     * 总评论数
     *
     * @returns {*}
     * @memberof IBIZProPlugin
     */
    commentcount?: any;
}
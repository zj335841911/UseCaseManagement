/**
 * 文档
 *
 * @export
 * @interface IBzDoc
 */
export interface IBzDoc {

    /**
     * 文档标识
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    ibzdocid?: any;

    /**
     * 由谁添加
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    addedby?: any;

    /**
     * 由谁更新
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    editedby?: any;

    /**
     * 添加时间
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    addeddate?: any;

    /**
     * 大小
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    size?: any;

    /**
     * 所属文档库
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    lib?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    createdate?: any;

    /**
     * 文档名称
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    ibzdocname?: any;

    /**
     * 排序
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    order?: any;

    /**
     * 是否已收藏
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    iscollect?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    createman?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    updatedate?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    editeddate?: any;

    /**
     * 对象类型
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    objecttype?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof IBzDoc
     */
    updateman?: any;
}
/**
 * 用例库
 *
 * @export
 * @interface IbzLib
 */
export interface IbzLib {

    /**
     * 描述
     *
     * @returns {*}
     * @memberof IbzLib
     */
    desc?: any;

    /**
     * 产品
     *
     * @returns {*}
     * @memberof IbzLib
     */
    product?: any;

    /**
     * 创建时间
     *
     * @returns {*}
     * @memberof IbzLib
     */
    addeddate?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof IbzLib
     */
    id?: any;

    /**
     * 最后编辑时间
     *
     * @returns {*}
     * @memberof IbzLib
     */
    lastediteddate?: any;

    /**
     * 由谁创建
     *
     * @returns {*}
     * @memberof IbzLib
     */
    addedby?: any;

    /**
     * 已删除
     *
     * @returns {*}
     * @memberof IbzLib
     */
    deleted?: any;

    /**
     * 名称
     *
     * @returns {*}
     * @memberof IbzLib
     */
    name?: any;

    /**
     * 类型
     *
     * @returns {*}
     * @memberof IbzLib
     */
    type?: any;

    /**
     * 最后编辑人
     *
     * @returns {*}
     * @memberof IbzLib
     */
    lasteditedby?: any;
}
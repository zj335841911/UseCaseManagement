/**
 * 产品生命周期
 *
 * @export
 * @interface ProductLife
 */
export interface ProductLife {

    /**
     * 平台/分支
     *
     * @returns {*}
     * @memberof ProductLife
     */
    branch?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof ProductLife
     */
    createman?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof ProductLife
     */
    updatedate?: any;

    /**
     * 产品生命周期名称
     *
     * @returns {*}
     * @memberof ProductLife
     */
    productlifename?: any;

    /**
     * 产品
     *
     * @returns {*}
     * @memberof ProductLife
     */
    product?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof ProductLife
     */
    createdate?: any;

    /**
     * 父对象
     *
     * @returns {*}
     * @memberof ProductLife
     */
    parent?: any;

    /**
     * 年
     *
     * @returns {*}
     * @memberof ProductLife
     */
    year?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof ProductLife
     */
    updateman?: any;

    /**
     * 属性
     *
     * @returns {*}
     * @memberof ProductLife
     */
    type?: any;

    /**
     * 里程碑
     *
     * @returns {*}
     * @memberof ProductLife
     */
    marker?: any;

    /**
     * 开始日期
     *
     * @returns {*}
     * @memberof ProductLife
     */
    begin?: any;

    /**
     * 产品生命周期标识
     *
     * @returns {*}
     * @memberof ProductLife
     */
    productlifeid?: any;

    /**
     * 结束日期
     *
     * @returns {*}
     * @memberof ProductLife
     */
    end?: any;
}
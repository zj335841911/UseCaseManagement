/**
 * 产品线
 *
 * @export
 * @interface ProductLine
 */
export interface ProductLine {

    /**
     * 产品线名称
     *
     * @returns {*}
     * @memberof ProductLine
     */
    productlinename?: any;

    /**
     * 产品线标识
     *
     * @returns {*}
     * @memberof ProductLine
     */
    productlineid?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof ProductLine
     */
    updateman?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof ProductLine
     */
    updatedate?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof ProductLine
     */
    createdate?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof ProductLine
     */
    createman?: any;
}
/**
 * 产品团队
 *
 * @export
 * @interface PRODUCTTEAM
 */
export interface PRODUCTTEAM {

    /**
     * 成员状态
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    teamstatus?: any;

    /**
     * 用户
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    account?: any;

    /**
     * 加盟日
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    join?: any;

    /**
     * 预计剩余
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    left?: any;

    /**
     * 总计可用
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    total?: any;

    /**
     * 结束时间
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    end?: any;

    /**
     * 用户
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    username?: any;

    /**
     * 最初预计
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    estimate?: any;

    /**
     * 可用工时/天
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    hours?: any;

    /**
     * 任务数
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    taskcnt?: any;

    /**
     * 团队类型
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    type?: any;

    /**
     * 总计消耗
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    consumed?: any;

    /**
     * 可用工日
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    days?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    id?: any;

    /**
     * 当前负责人
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    leadingcadre?: any;

    /**
     * 受限用户
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    limited?: any;

    /**
     * 排序
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    order?: any;

    /**
     * 角色
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    role?: any;

    /**
     * 产品编号
     *
     * @returns {*}
     * @memberof PRODUCTTEAM
     */
    root?: any;
}
/**
 * 项目产品
 *
 * @export
 * @interface ProjectProduct
 */
export interface ProjectProduct {

    /**
     * 虚拟主键
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    id?: any;

    /**
     * 产品
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    productname?: any;

    /**
     * 项目
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    projectname?: any;

    /**
     * 计划名称
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    planname?: any;

    /**
     * 产品
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    product?: any;

    /**
     * 产品计划
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    plan?: any;

    /**
     * 平台/分支
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    branch?: any;

    /**
     * 项目
     *
     * @returns {*}
     * @memberof ProjectProduct
     */
    project?: any;
}
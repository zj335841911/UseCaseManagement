/**
 * 需求描述
 *
 * @export
 * @interface StorySpec
 */
export interface StorySpec {

    /**
     * 需求描述	
     *
     * @returns {*}
     * @memberof StorySpec
     */
    spec?: any;

    /**
     * 验收标准
     *
     * @returns {*}
     * @memberof StorySpec
     */
    verify?: any;

    /**
     * 虚拟主键
     *
     * @returns {*}
     * @memberof StorySpec
     */
    id?: any;

    /**
     * 需求名称
     *
     * @returns {*}
     * @memberof StorySpec
     */
    title?: any;

    /**
     * 版本号
     *
     * @returns {*}
     * @memberof StorySpec
     */
    version?: any;

    /**
     * 需求
     *
     * @returns {*}
     * @memberof StorySpec
     */
    story?: any;
}
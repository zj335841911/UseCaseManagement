/**
 * 单位
 *
 * @export
 * @interface SysOrganization
 */
export interface SysOrganization {

    /**
     * 单位标识
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    orgid?: any;

    /**
     * 单位代码
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    orgcode?: any;

    /**
     * 名称
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    orgname?: any;

    /**
     * 上级单位
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    parentorgid?: any;

    /**
     * 单位简称
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    shortname?: any;

    /**
     * 单位级别
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    orglevel?: any;

    /**
     * 排序
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    showorder?: any;

    /**
     * 上级单位
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    parentorgname?: any;

    /**
     * 区属
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    domains?: any;

    /**
     * 逻辑有效
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    enable?: any;

    /**
     * 创建时间
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    createdate?: any;

    /**
     * 最后修改时间
     *
     * @returns {*}
     * @memberof SysOrganization
     */
    updatedate?: any;
}
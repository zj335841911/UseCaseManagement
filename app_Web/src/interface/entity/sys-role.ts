/**
 * 系统角色
 *
 * @export
 * @interface SysRole
 */
export interface SysRole {

    /**
     * 角色标识
     *
     * @returns {*}
     * @memberof SysRole
     */
    roleid?: any;

    /**
     * 角色名称
     *
     * @returns {*}
     * @memberof SysRole
     */
    rolename?: any;

    /**
     * 备注
     *
     * @returns {*}
     * @memberof SysRole
     */
    memo?: any;

    /**
     * 父角色标识
     *
     * @returns {*}
     * @memberof SysRole
     */
    proleid?: any;

    /**
     * 父角色名称
     *
     * @returns {*}
     * @memberof SysRole
     */
    prolename?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof SysRole
     */
    createdate?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof SysRole
     */
    updatedate?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof SysRole
     */
    createman?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof SysRole
     */
    updateman?: any;
}
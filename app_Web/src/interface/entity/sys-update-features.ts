/**
 * 系统更新功能
 *
 * @export
 * @interface SysUpdateFeatures
 */
export interface SysUpdateFeatures {

    /**
     * 系统更新功能名称
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    sysupdatefeaturesname?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    updatedate?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    createman?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    updateman?: any;

    /**
     * 更新类型
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    type?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    createdate?: any;

    /**
     * 更新功能
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    upfeatures?: any;

    /**
     * 系统更新功能标识
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    sysupdatefeaturesid?: any;

    /**
     * 展示顺序
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    displayorder?: any;

    /**
     * 功能描述
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    featuresdesc?: any;

    /**
     * 所属更新
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    sysupdatelogname?: any;

    /**
     * 系统更新日志标识
     *
     * @returns {*}
     * @memberof SysUpdateFeatures
     */
    sysupdatelogid?: any;
}
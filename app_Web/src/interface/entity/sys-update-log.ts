/**
 * 系统更新日志
 *
 * @export
 * @interface SysUpdateLog
 */
export interface SysUpdateLog {

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    updatedate?: any;

    /**
     * 更新人
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    updateman?: any;

    /**
     * 建立人
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    createman?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    createdate?: any;

    /**
     * 系统更新日志标识
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    sysupdatelogid?: any;

    /**
     * 更新平台
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    updatebranch?: any;

    /**
     * 更新功能列表详情
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    updatefeatures?: any;

    /**
     * 更新说明
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    updesc?: any;

    /**
     * 更新名称
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    sysupdatelogname?: any;

    /**
     * 最新更新
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    latestupdate?: any;

    /**
     * 更新日期
     *
     * @returns {*}
     * @memberof SysUpdateLog
     */
    update?: any;
}
/**
 * 用户角色关系
 *
 * @export
 * @interface SysUserRole
 */
export interface SysUserRole {

    /**
     * 用户角色关系标识
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    userroleid?: any;

    /**
     * 角色标识
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    roleid?: any;

    /**
     * 角色名称
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    rolename?: any;

    /**
     * 用户标识
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    userid?: any;

    /**
     * 用户名称
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    personname?: any;

    /**
     * 登录名
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    loginname?: any;

    /**
     * 单位
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    orgname?: any;

    /**
     * 主部门
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    mdeptname?: any;

    /**
     * 建立时间
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    createdate?: any;

    /**
     * 更新时间
     *
     * @returns {*}
     * @memberof SysUserRole
     */
    updatedate?: any;
}
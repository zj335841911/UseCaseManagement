/**
 * 任务统计
 *
 * @export
 * @interface TaskStats
 */
export interface TaskStats {

    /**
     * 任务截至日期
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskdeadline?: any;

    /**
     * 任务编号
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskid?: any;

    /**
     * 任务实际开始时间
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskrealstart?: any;

    /**
     * 效率
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskefficient?: any;

    /**
     * 任务预计消耗
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskestimate?: any;

    /**
     * 结束
     *
     * @returns {*}
     * @memberof TaskStats
     */
    end?: any;

    /**
     * 用户总消耗
     *
     * @returns {*}
     * @memberof TaskStats
     */
    userconsumed?: any;

    /**
     * 剩余总工时
     *
     * @returns {*}
     * @memberof TaskStats
     */
    totalleft?: any;

    /**
     * 项目总消耗
     *
     * @returns {*}
     * @memberof TaskStats
     */
    projectconsumed?: any;

    /**
     * 属性
     *
     * @returns {*}
     * @memberof TaskStats
     */
    begin?: any;

    /**
     * 消耗总工时
     *
     * @returns {*}
     * @memberof TaskStats
     */
    totalconsumed?: any;

    /**
     * 预计总工时
     *
     * @returns {*}
     * @memberof TaskStats
     */
    totalestimate?: any;

    /**
     * 任务预计开始日期
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskeststarted?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof TaskStats
     */
    id?: any;

    /**
     * 部门
     *
     * @returns {*}
     * @memberof TaskStats
     */
    dept?: any;

    /**
     * 名称
     *
     * @returns {*}
     * @memberof TaskStats
     */
    name?: any;

    /**
     * 项目名称
     *
     * @returns {*}
     * @memberof TaskStats
     */
    projectname?: any;

    /**
     * 项目
     *
     * @returns {*}
     * @memberof TaskStats
     */
    project?: any;

    /**
     * 任务实际完成日期
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskfinisheddate?: any;

    /**
     * 任务名称
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskname?: any;

    /**
     * 总任务数
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskcnt?: any;

    /**
     * 任务优先级
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskpri?: any;

    /**
     * 任务延期
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskdelay?: any;

    /**
     * 完成者
     *
     * @returns {*}
     * @memberof TaskStats
     */
    finishedby?: any;
}
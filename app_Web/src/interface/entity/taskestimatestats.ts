/**
 * 任务工时统计
 *
 * @export
 * @interface taskestimatestats
 */
export interface taskestimatestats {

    /**
     * 日期
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    date?: any;

    /**
     * 年
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    year?: any;

    /**
     * 消耗的工时
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    consumed?: any;

    /**
     * 评估状态
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    evaluationstatus?: any;

    /**
     * 用户
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    account?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    id?: any;

    /**
     * 月（显示）
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    monthname?: any;

    /**
     * 项目名称
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    name?: any;

    /**
     * 年（显示）
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    yearname?: any;

    /**
     * 评估工时
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    evaluationtime?: any;

    /**
     * 评估成本
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    evaluationcost?: any;

    /**
     * 投入成本
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    inputcost?: any;

    /**
     * 月
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    month?: any;

    /**
     * 任务数
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    taskcnt?: any;
}
/**
 * 测试运行
 *
 * @export
 * @interface TestRun
 */
export interface TestRun {

    /**
     * 结果
     *
     * @returns {*}
     * @memberof TestRun
     */
    lastrunresult?: any;

    /**
     * 最后执行时间
     *
     * @returns {*}
     * @memberof TestRun
     */
    lastrundate?: any;

    /**
     * 指派给
     *
     * @returns {*}
     * @memberof TestRun
     */
    assignedto?: any;

    /**
     * 最后执行人
     *
     * @returns {*}
     * @memberof TestRun
     */
    lastrunner?: any;

    /**
     * 当前状态
     *
     * @returns {*}
     * @memberof TestRun
     */
    status?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof TestRun
     */
    id?: any;

    /**
     * 用例版本
     *
     * @returns {*}
     * @memberof TestRun
     */
    version?: any;

    /**
     * 测试用例
     *
     * @returns {*}
     * @memberof TestRun
     */
    ibizcase?: any;

    /**
     * 测试单
     *
     * @returns {*}
     * @memberof TestRun
     */
    task?: any;
}